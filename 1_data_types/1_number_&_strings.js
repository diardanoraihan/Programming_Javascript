// ========================================= Number =========================================
// Javascript does not differentiate any type of number like integer, float, etc.
// Hence, you can just run your code right away

console.log(10%6);
console.log(10+6*2);
console.log(typeof 10);
console.log(typeof "10");

// Math built-in function can be handy
// return number between 0 - 1
console.log(Math.random()); 
// obtain pi value
console.log(Math.PI);

// parseInt changes one data type to number (+ is the shortcut)
console.log(parseInt("10")+213);
console.log(+"10")
// take this number based on based 10
console.log(parseInt("10", 10));
// produce NaN if the program cannot identify the input as number
console.log(parseInt("hello"))
console.log(isNaN("hello"));

// ======================================= Strings =======================================
// Javascript has strings as a sequence of Unicode characters
console.log("Hello");
console.log("Super duper".length);
console.log("Diar".charAt(0));
console.log("Diar"+"dano"+" Raihan");
console.log("i want to be uppercase!!!".toUpperCase());





